#include "libft.h"

int		ft_is_character(char *str, int c)
{
	int i;

	i = 0;
	c = (unsigned char)c;
	while (str[i] != '\0')
	{
		if (str[i] == c)
		{
			return (1);
		}
		i++;
	}
	return (0);
}
