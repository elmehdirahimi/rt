#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	int					i;
	unsigned char		*ss;

	i = 0;
	ss = (unsigned char *)s;
	while (i < (int)n)
	{
		ss[i] = '\0';
		i++;
	}
}
