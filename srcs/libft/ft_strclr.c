#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;
	int		j;

	if (s)
	{
		i = ft_strlen(s);
		j = 0;
		while (j <= (int)i)
		{
			s[j] = '\0';
			j++;
		}
	}
}
